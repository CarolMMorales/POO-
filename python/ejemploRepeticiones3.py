class Incremento():
    contador = 1; #1
    def incrementar(cantidadNumeros):
        while(Incremento.contador <= cantidadNumeros):#2
            if (Incremento.contador % 2 != 0):#incremento es impar
                print(Incremento.contador)
            Incremento.contador = Incremento.contador + 1;

#cliente
Incremento.incrementar(1000);


        
